# Napisz program, który sprawdzi, czy podana przez użytkownika liczba jest:
# a) podzielna przez 2;
# b) większa od 5;
# c) mniejsza lub równa 20;
a = float(input('Podaj dowolną liczbę: '))
print()
print(f'Podzielna przez 2: {a%2==0}')
print(f'Większe od 5: {a>15}')
print(f'Mniejsza lub równa 20: {a<=20}')
